package com.lxit.crm.entity;

import java.io.Serializable;

/**
 * 销售机会
 * @author Administrator
 *
 */
public class Chance implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	
	private int id;//机会主键
	
	private String chanceNumber;//机会编号
	
	private String chanceName;//机会名称
	
	private String chanceContext;//机会内容
	
	private String chanceType;//机会类型
	
	private String startDate;//录入时间
	
	private String userName;//录入人id
	
	private String lastName;//最后修改人
	
	private String lastDateTime;//最后修改时间
	
	private String chanceallot;//执行者用户
	
	private String remark;//备注

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getChanceNumber() {
		return chanceNumber;
	}

	public void setChanceNumber(String chanceNumber) {
		this.chanceNumber = chanceNumber;
	}

	public String getChanceName() {
		return chanceName;
	}

	public void setChanceName(String chanceName) {
		this.chanceName = chanceName;
	}

	public String getChanceContext() {
		return chanceContext;
	}

	public void setChanceContext(String chanceContext) {
		this.chanceContext = chanceContext;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}


	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getLastDateTime() {
		return lastDateTime;
	}

	public void setLastDateTime(String lastDateTime) {
		this.lastDateTime = lastDateTime;
	}

	public String getChanceallot() {
		return chanceallot;
	}

	public void setChanceallot(String chanceallot) {
		this.chanceallot = chanceallot;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getChanceType() {
		return chanceType;
	}

	public void setChanceType(String chanceType) {
		this.chanceType = chanceType;
	}

	@Override
	public String toString() {
		return "Chance [id=" + id + ", chanceNumber=" + chanceNumber + ", chanceName=" + chanceName + ", chanceContext="
				+ chanceContext + ", chanceType=" + chanceType + ", startDate=" + startDate + ", userName=" + userName
				+ ", lastName=" + lastName + ", lastDateTime=" + lastDateTime + ", chanceallot=" + chanceallot
				+ ", remark=" + remark + "]";
	}

	
	
	
}
